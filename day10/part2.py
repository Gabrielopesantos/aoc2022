INPUT_TXT = os.path.join(os.path.dirname(__file__), 'input.txt')

OPP_CYCLES = {
    "addx": 2,
    "noop": 1,
}

crt = ["." for _ in range(6 * 40)]


def compute(s: str) -> int:
    lines = s.splitlines()
    x = 1
    total_cycles = 0
    for line in lines:
        opp, *rest = line.split()
        opp_val = int(rest[0]) if rest else 0
        opp_cycles = OPP_CYCLES[opp]

        while opp_cycles:
            opp_cycles -= 1
            row_pos = total_cycles
            if total_cycles > 39:
                row_pos = (total_cycles % 40)
            if x == row_pos:
                crt[total_cycles] = "#"
            elif x - 1 == row_pos:
                crt[total_cycles] = "#"
            elif x + 1 == row_pos:
                crt[total_cycles] = "#"

            total_cycles += 1
        x += opp_val

    for end in range(40, 40*7, 40):
        init = end - 40
        print("".join(crt[init:end]))
    return None


